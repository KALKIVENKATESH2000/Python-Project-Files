# Python program to convert Celsius to Fahrenheit.

celsius = float (input("Temperature value in degree Celsius: " ))  
Fahrenheit = (celsius * 9/5) + 32 # Formula for Fahrenheit from the Celsius

#print ('The %.2f degree Celsius is equal to: %.2f Fahrenheit'  
#%(celsius, Fahrenheit)) 

print("The", celsius, "degree clesius is equal:", Fahrenheit, "Fahrenheit.")
